qnr_t divideWithMod(volatile unsigned int dividend, volatile unsigned int divisor)
{
    volatile unsigned int remainder;
    volatile unsigned int quotient;
    //    volatile unsigned int modulus;
    volatile unsigned int dividend_high = 0;
    // volatile unsigned int low;
    
    __asm__("divl %2;"
            : "=d" (remainder), "=a" (quotient)
            : "g" (divisor), "d" (dividend_high), "a" (dividend));
    
    qnr_t rval = { quotient, remainder};
    return(rval);
}


// calculate digits sum
char digitSum(short x, short y)
{
    qnr_t x1, y1, x2, y2;
    x2 = divideWithMod(x, 100);
    x1 = divideWithMod(x2.remainder, 10);
    y2 = divideWithMod(y, 100);
    y1 = divideWithMod(y2.remainder, 10);
    
//    printf("x = %d, 100s = %d, 10s = %d, 1s = %d, ", x, x2.quotient, x1.quotient, x1.remainder);
//    printf("y = %d, 100s = %d, 10s = %d, 1s = %d, ", y, y2.quotient, y1.quotient, y1.remainder);
//    printf("ret = %d\n", x2.quotient + x1.quotient + x1.remainder + y2.quotient + y1.quotient + y1.remainder);
    
    return(x2.quotient + x1.quotient + x1.remainder + y2.quotient + y1.quotient + y1.remainder);
    
}

#define GRIDSIZE 299
int main(int argc, const char * argv[]) {

    char buf[2][GRIDSIZE + 1];
    char * last;
    char * curr;
    char seen, lstc;
    short i, j, jj, lstp;
    unsigned int total = 0;

    
    // We know that the negative digits are still calculated as positive digits as absolute values are used.
    // Rotation of one quadrant is same matching as x < 0 or y < 0 -- so let's figure out one quadrant
    // since we are limited to a diamond shape (by the digit sums <= 19), with a max of 299
    
    // clear cells are those with digit sums that are <= 19
    // blocked cell are those with digit sums > 20
    // trapped cells are islands in the middle of blocked cells
    
    // Init buffer to refer back to in order to find trapped cells (see the appended data '.' marks)
    for(i = 0; i < GRIDSIZE; i++) {
        buf[0][i] = buf[1][i] = ' ';
//        printf("%d", divideWithMod(i, 10).remainder);
    }
 //   printf("\n");
    
    // make diamond's quadrant (0,0), (0, 299), (299, 0)
    for(i = 0; i < GRIDSIZE; i++) {
        // alternate buffers (last line and current line) for reference when removing trapped cells
        if(i & 1) {
            last = &buf[1][0];
            curr = &buf[0][0];
        }
        else {
            last = &buf[0][0];
            curr = &buf[1][0];
        }
        seen = 0;
        lstc = ' ';
        lstp = 0;
        // build rows(i or x) and columns(j or y)
        for(j = 1; j < GRIDSIZE - i; j++) {
            if(digitSum((int) i, (int) j) <= 19) {
                curr[j] = ' ';
                total++; // let's use this to keep a total of all clear cells
            }
            else {
                curr[j] = '*';
            }

            // if transition from clear to blocked and we have already seen a blocked to clear transition
            if((seen) && (lstc == ' ') && (curr[j] != ' ')) {
//                printf("Last = %d\n", lstp);
                if((curr[lstp] == ' ') && (last[lstp] != ' ')) {
                    for(jj = lstp; jj < j; jj++) {
                        curr[jj] = '.';
                        total--;  // we guessed these were clear, but they're inaccessible - remove them
                    }
                }
            }
            else {
                // other wise check if a transition from blocked to clear happened and note the position
                if((lstc != ' ') && (curr[j] == ' ')) {
                    seen = !0;
                    lstp = j;
                }
            }
            lstc = curr[j];
           // printf("%c", curr[j]);
        }
        //curr[j] = '\0';
//        printf("%s - %d\n", curr, i);
    }
//    for(i = 0; i < GRIDSIZE; i++) {
//        printf("%d", divideWithMod(i, 10).remainder);
//    }
//    printf("\n");
    
    // take off first col or first row and mult x 4 -- add one for the origin
//    printf("Total = ");
    printf("%d\n", (total << 2) + 1);
    return 0;
 }

